public class UseThree {

    public static void main(String[] args) {
        args = new String[3];
        args[0] = "Nguyen";
        args[1] = "Dang";
        args[2] = "Dao";
        System.out.print("Hi, ");
        System.out.print(args[2]);
        System.out.print(", ");
        System.out.print(args[1]);
        System.out.print(", and ");
        System.out.print(args[0]);
        System.out.println(".");
    }

}